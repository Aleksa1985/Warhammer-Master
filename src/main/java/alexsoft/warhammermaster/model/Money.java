
package alexsoft.warhammermaster.model;

public class Money {
    
    private Integer goldAmount;
    private Integer silverAmount;
    private Integer copperAmount;

    public Integer getGoldAmount() {
        return goldAmount;
    }

    public void setGoldAmount(Integer goldAmount) {
        this.goldAmount = goldAmount;
    }

    public Integer getSilverAmount() {
        return silverAmount;
    }

    public void setSilverAmount(Integer silverAmount) {
        this.silverAmount = silverAmount;
    }

    public Integer getCopperAmount() {
        return copperAmount;
    }

    public void setCopperAmount(Integer copperAmount) {
        this.copperAmount = copperAmount;
    }

    
    
}
